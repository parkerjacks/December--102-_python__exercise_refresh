""" 
For these exercises, use a for loop.

Print the numbers from 0 to 99


Print the even numbers from 0 to 99


Print the odd numbers from 0 to 99 

"""
#Print the numbers from 0 to 99

for x in range(99 + 1): 
    print(x) 


#Print the even numbers from 0 to 99

for x in range(99 + 1): 
    if x % 2 == 0: 
        print(x)

# Print the odd numbers from 0 to 99 
for x in range(99 + 1): 
    if x % 2 == 1: 
        print(x)